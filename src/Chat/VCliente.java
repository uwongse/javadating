    private void jlDesconectarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlDesconectarMouseClicked
        // TODO add your handling code here:
        if (cliente != null) {
            cliente.enviarTrama(3, "");
            cliente.interrupt();
            servidor.interrupt();
            this.dispose();
        }
        cliente = null;
        jlConectar.setEnabled(true);
        jlDesconectar.setEnabled(false);
        dlm.removeAllElements();
        jtaChat.setText("");
        servidor.interrupt();
        this.dispose();
    }//GEN-LAST:event_jlDesconectarMouseClicked


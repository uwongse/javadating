    private void jlConectarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlConectarMouseClicked
        // TODO add your handling code here:
        jlLista.setModel(dlm);
        try {
            int puerto = servidor.getPort();

            String nick = jtfNick.getText();
            if (cliente == null) {
                cliente = new MCliente(puerto, "127.0.0.1", nick, this);
                cliente.start();
            }
            jlConectar.setEnabled(false);
            jlDesconectar.setEnabled(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en la entrada de datos");
            cliente = null;
        }
    }//GEN-LAST:event_jlConectarMouseClicked


    private void jlImagenUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlImagenUserMouseClicked
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            int resultado;

            VistaChooser vc = new VistaChooser(this, true);

            FileNameExtensionFilter formato = new FileNameExtensionFilter("JPG, PNG y GIF", "jpg", "png", "gif");

            vc.jfcImg.setFileFilter(formato);

            resultado = vc.jfcImg.showOpenDialog(null);

            if (JFileChooser.APPROVE_OPTION == resultado) {

                archivo = VistaChooser.jfcImg.getSelectedFile();

                url = archivo.getAbsolutePath();

                archivo = new File(url);

                ImageIcon imgIcon = new ImageIcon(archivo.toString());

                Icon icono = new ImageIcon(imgIcon.getImage().getScaledInstance(jlImagenUser.getWidth(), jlImagenUser.getHeight(), Image.SCALE_DEFAULT));

                jlImagenUser.setIcon(icono);

                fis = new FileInputStream(archivo);
            }

        } catch (Exception ex) {
        }
    }//GEN-LAST:event_jlImagenUserMouseClicked


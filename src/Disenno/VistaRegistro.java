    private void jbSiguienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbSiguienteMouseClicked
        // TODO add your handling code here:
        String regex = "^[A-Za-z0-9+_.-]+@(.+)$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(jtfCorreo.getText());

        if (jtfNombre.getText().isEmpty() || jtfCorreo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Campo vacío");
        } else {
            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(this, "Inserte un correo válido");
            } else {
                if (jpfContrasenna.getPassword().length != 0 && Arrays.equals(jpfContrasenna.getPassword(), jpfRepetirCon.getPassword())) {
                    cardLayout.next(jpContenedor);
                } else {
                    jlPass.setVisible(true);
                }
            }

        }
    }//GEN-LAST:event_jbSiguienteMouseClicked


    private void jlFinalizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlFinalizarMouseClicked
        // TODO add your handling code here:
        if (jtfTelefono.getText().isEmpty() || jtfEdad.getText().isEmpty() || jcbCiudad.getSelectedIndex() == 0) {

            JOptionPane.showMessageDialog(this, "Campo vacío");

        } else {

            if (Integer.parseInt(jtfEdad.getText()) < 18) {

                JOptionPane.showMessageDialog(this, "Prohibido para menores de 18");

                this.dispose();

            } else {

                if (url == null) {

                    JOptionPane.showMessageDialog(this, "Imagen requerida");

                } else {
                    JOptionPane.showMessageDialog(this, "Registro completado");
                    //CREAR CLIENTE
                    int darLike = 0;
                    int recibirLike = 0;
                   
                    c = new Cliente(sexo, Integer.parseInt(jtfEdad.getText()), orientacion, jcbCiudad.getSelectedItem().toString(), jtfTelefono.getText(),
                            fis, darLike, recibirLike, jtfNombre.getText(), jtfCorreo.getText(), Arrays.toString(jpfContrasenna.getPassword()), "cliente");

                    sistema.crearUsuario(c);
                    sistema.crearCliente(c, url);
                    VistaInicio vi = new VistaInicio();

                    vi.setVisible(true);

                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_jlFinalizarMouseClicked

